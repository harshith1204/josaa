// Helper to format institute with NIRF rank
export function formatInstituteRank(institute) {
  const data = nirfRankings[institute];
  if (!data) return { name: institute, nirf: null, typeRank: null, estd: null };
  let typeRank = null;
  if (data.nit) typeRank = "NIT-" + data.nit;
  else if (data.iiit) typeRank = "IIIT-" + data.iiit;
  return { name: institute, nirf: data.nirf, typeRank, estd: data.estd };
}

// Helper to get branch emoji
export function getBranchEmoji(program) {
  const p = program.toLowerCase();
  if (p.includes('cse') || p.includes('computer science')) return '\u{1F4BB}';
  if (p.includes('it ') || p.includes('information technology')) return '\u{1F4F1}';
  if (p.includes('ai') || p.includes('artificial intelligence')) return '\u{1F916}';
  if (p.includes('data science') || p.includes(' ds ')) return '\u{1F4CA}';
  if (p.includes('ece') || p.includes('electronics')) return '\u{1F50C}';
  if (p.includes('eee') || p.includes('electrical')) return '\u26A1';
  if (p.includes('vlsi')) return '\u{1F52C}';
  if (p.includes('mechanical')) return '\u2699\uFE0F';
  if (p.includes('civil')) return '\u{1F3D7}\uFE0F';
  if (p.includes('chemical')) return '\u{1F9EA}';
  if (p.includes('biotech')) return '\u{1F9EC}';
  if (p.includes('agricultural') || p.includes('agri')) return '\u{1F33E}';
  return '\u{1F393}';
}

// Format rank delta
export function formatDelta(delta) {
  const abs = Math.abs(delta);
  if (abs >= 1000) {
    const k = (delta / 1000).toFixed(1);
    return (delta >= 0 ? '+' : '') + k + 'k';
  }
  return (delta >= 0 ? '+' : '') + delta.toLocaleString();
}
